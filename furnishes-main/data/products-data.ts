// Helper function to get local image path
const getImagePath = (imageName: string) => {
  return `/images/${imageName}`;
};

// Re-export Product type from types folder
export type { Product } from '@/types/product';

// Function to get full product details
export const getProductDetails = (product: Product): Product => {
  // If product already has details, return as is
  if (product.ref) return product;

  // Otherwise, generate default details
  return {
    ...product,
    ref: `${product.type.substring(0, 3).toUpperCase()}-${product.name.substring(0, 3).toUpperCase()}-${product.id}`,
    description: `A beautifully designed ${product.name.toLowerCase()} featuring ${product.style.join(' and ').toLowerCase()} aesthetics. Crafted with attention to detail and quality materials.`,
    additionalInfo: 'Available in multiple options. Natural materials may vary slightly in color and texture.',
    rating: 4.5 + Math.random() * 0.5, // Random rating between 4.5 and 5.0
    reviewCount: Math.floor(Math.random() * 50) + 20,
    sizes: product.type.includes('Sofa') ? ['2-Seat', '3-Seat', 'Sectional'] : ['Standard'],
    colors: product.color.map((c, i) => ({
      name: ['Charcoal', 'Dark Gray', 'Mustard', 'Beige', 'Green', 'Navy', 'Brown'][i] || 'Custom',
      value: c,
    })),
    images: [
      product.image,
      product.image, // Use same image for now, can be updated with more images later
      product.image,
      product.image,
      product.image,
    ],
    dimensions: {
      height: '85 cm / 33.5 in',
      width: '220 cm / 86.6 in',
      depth: '95 cm / 37.4 in',
      seatHeight: '45 cm / 17.7 in',
      seatDepth: '58 cm / 22.8 in',
      weight: '68 kg / 150 lbs',
    },
    materials: product.material.map(m => ({
      name: m,
      value: `Premium ${m}`,
    })),
    fullDescription: `The ${product.name} represents the pinnacle of contemporary design, combining ${product.style.join(' and ').toLowerCase()} principles with uncompromising comfort. Each piece is crafted with meticulous attention to detail, featuring hand-finished details and precision construction.`,
    delivery: 'In Stock - Ships in 3-5 business days. White glove delivery included.',
    care: 'Follow manufacturer care instructions. Clean regularly with appropriate products.',
    warranty: '5-Year Limited Warranty covering manufacturing defects.',
  };
};

